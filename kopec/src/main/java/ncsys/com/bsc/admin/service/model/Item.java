package ncsys.com.bsc.admin.service.model;

public class Item {

	private String idx;
	private String code;
	private String itemname;
	private String measureid;

	private String unit;
	private String itementry;
	private String itemtype;
	private String itemfixed;



	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getItementry() {
		return itementry;
	}
	public void setItementry(String itementry) {
		this.itementry = itementry;
	}
	public String getItemtype() {
		return itemtype;
	}
	public void setItemtype(String itemtype) {
		this.itemtype = itemtype;
	}
	public String getItemfixed() {
		return itemfixed;
	}
	public void setItemfixed(String itemfixed) {
		this.itemfixed = itemfixed;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getItemname() {
		return itemname;
	}
	public void setItemname(String itemname) {
		this.itemname = itemname;
	}
	public String getMeasureid() {
		return measureid;
	}
	public void setMeasureid(String measureid) {
		this.measureid = measureid;
	}
	public String getIdx() {
		return idx;
	}
	public void setIdx(String idx) {
		this.idx = idx;
	}



}
